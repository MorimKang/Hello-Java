package exam.oop9;

public class ShapeTest {
	public static void main(String[] args) {
		printArea(new Circle());
		printArea(new Rectangle());
		printArea(new Triangle());
	}

	private static void printArea(Shape s) { 
		// printArea의 파라미터를 Shape으로 줘서 어떤 타입의 도형이 와도 
		//이 함수 안에서 업캐스팅으로 처리하도록하면 printArea함수를 한번만 만들어도 된다.
		s.area();
		if(s instanceof Circle) { //instanceof
			Circle c = (Circle) s;
			System.out.println("반지름이 "+ c.r +"인 원의 넓이는 "+ c.res+"입니다.");			
		}else if(s instanceof Rectangle) {
			Rectangle r = (Rectangle) s;
			System.out.println("가로가 " + r.w +"이고, 세로가 " + r.h+"인 사각형의 넓이는 "+ r.res+"입니다.");
		}else if(s instanceof Triangle) {
			Triangle t = (Triangle) s;
			System.out.println("밑변이 " + t.w +"이고, 높이가 " + t.h+"인 삼각형의 넓이는 "+ t.res+"입니다.");
		}	
	}
}
